#include <iostream>
using namespace std;

// Этап 1: Функция swap для обмена значениями двух переменных
// Функция принимает два аргумента по ссылке, чтобы изменить их значения в вызывающей программе
void swap(int& a, int& b) {
    int temp = a;  // Сохраняем значение a во временную переменную
    a = b;         // Присваиваем a значение b
    b = temp;      // Присваиваем b значение временной переменной (старое значение a)
}

int main() {
    // Этап 2: Объявление переменных
    int num1, num2;

    // Этап 3: Ввод значений для переменных
    cout << "Enter first number: ";
    cin >> num1;
    cout << "Enter second number: ";
    cin >> num2;

    // Этап 4: Вывод значений до обмена
    cout << "Before swap: num1 = " << num1 << ", num2 = " << num2 << endl;

    // Этап 5: Вызов функции swap для обмена значениями
    swap(num1, num2);

    // Этап 6: Вывод значений после обмена
    cout << "After swap: num1 = " << num1 << ", num2 = " << num2 << endl;

    return 0;  // Завершаем программу
}
